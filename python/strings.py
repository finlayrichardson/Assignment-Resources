string = "monke"

# First character
print(string[0])  # 'm'

# Last character
print(string[-1])  # 'e'

# Specific character
print(string[1])  # 'o'

# Characters from certain point to end of string
print(string[2:])  # 'nke'

# Characters from start of string to certain point
print(string[:2])  # 'mo'

# Specific range of characters
print(string[2:4])  # 'nk'

# Last character removed
print(string[:-1])  # 'monk'

# First character removed
print(string[1:])  # 'onke'
